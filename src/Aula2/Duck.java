package Aula2;

public class Duck {
	
	String asas = "voando...";
	String bico = "assobiando...";
	String pata = "andando... ";
	
	public void Voar() {
		System.out.println(this.asas);
	}
	
	public void Assobiar() {
		System.out.println(this.bico);
	}
	
	public void Andar() {
		System.out.println(this.pata);
	}

}
